// AIVibe/Core/AI/DesignNorms.swift
// Единый источник эргономических и бюджетных норм дизайна интерьера.
//
// Зачем: раньше нормы (ширина прохода, доставка, пороги) были захардкожены
// текстом в нескольких промптах (ContextBuilder, SkillIndex, PromptBuilder) и
// успели разойтись (проход был 70 см в одном месте и 80 см в другом). Теперь
// все промпты интерполируют значения отсюда — рассинхрон исключён by design.
//
// Источник цифр: российская эргономика расстановки мебели (проход для одного
// человека — 70 см, основные проходы — 90 см, зазор у мебели — 30 см).

import Foundation

/// Нормы дизайна интерьера — единый источник истины для всех промптов.
public enum DesignNorms {

    // MARK: - Проходы и зазоры (в сантиметрах)

    /// Минимальная ширина прохода для одного человека.
    public static let minPassageCm = 70

    /// Ширина основных проходов (где расходятся два человека).
    public static let mainPassageCm = 90

    /// Минимальный зазор между мебелью и батареей отопления.
    public static let furnitureToRadiatorCm = 30

    /// Минимальный зазор между мебелью и стеной (чтобы не упиралась).
    public static let furnitureToWallCm = 5

    /// Свободная зона перед дверью (линейно) — мебель не должна её перекрывать.
    /// Используется в AR-проверке коллизий (CollisionService).
    public static let doorClearanceFrontCm = 80

    // MARK: - Зоны (в квадратных метрах)

    /// Минимальная зона открывания двери, которую нельзя загораживать.
    public static let doorClearanceM2 = 1.0

    /// Порог «маленькой комнаты» — ниже рекомендуем светлые тона и минимализм.
    public static let smallRoomThresholdM2 = 15

    // MARK: - Бюджет

    /// Доля бюджета, неявно резервируемая под доставку (~10%).
    public static let deliveryShare = 0.10

    /// Порог «очень маленького бюджета» (₽) — предлагаем только необходимое.
    public static let lowBudgetRub = 30_000

    /// Минимальный приемлемый рейтинг товара при экономии.
    public static let minAcceptableRating = 4.0
}
